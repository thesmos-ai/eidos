// TestPruneCommand_PreservesManifestJSON is a sanity probe: after
// prune, the new manifest on disk is valid JSON. Catches
// serialisation regressions that would render the manifest
// unreadable on the next prune.
func TestPruneCommand_PreservesManifestJSON(t *testing.T) {
	t.Parallel()

	t.Run("post-prune manifest is valid JSON", func(t *testing.T) {
		t.Parallel()
		env, _, _ := freshEnv(t, "eidos")
		_, target := seedStaleFile(t, env.Workdir, env.Brand, "out", "stale.go")
		writePrevManifest(t, env, target)
		cmd := &cli.PruneCommand{Config: cli.PruneConfig{
			Plugins: []plugin.Plugin{
				stubFrontend{name: "fe"},
				stubBackend{name: "be", lang: "stub"},
			},
		}}
		if code := cmd.Execute(t.Context(), env); code != cli.ExitOK {
			t.Fatalf("Execute = %d, want ExitOK", code)
		}
		raw, err := os.ReadFile(env.ManifestPath())
		if err != nil {
			t.Fatalf("read manifest: %v", err)
		}
		var v any
		if err := json.Unmarshal(raw, &v); err != nil {
			t.Fatalf("manifest not valid JSON after prune: %v\n%s", err, raw)
		}
	})
}

