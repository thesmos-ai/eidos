// seedStaleFile writes a file with the canonical generated-by
// marker line and seeds the prev manifest with a matching entry.
// Used by every prune-test stanza that needs a "previously
// generated, no longer claimed" candidate on disk.
func seedStaleFile(
	t *testing.T,
	workdir, brand, dir, name string,
) (path string, target emit.Target) {
	t.Helper()
	if err := os.MkdirAll(filepath.Join(workdir, dir), 0o750); err != nil {
		t.Fatalf("mkdir: %v", err)
	}
	body := []byte("// Code generated by " + brand + ". DO NOT EDIT.\npackage x\n")
	path = filepath.Join(workdir, dir, name)
	if err := os.WriteFile(path, body, 0o600); err != nil {
		t.Fatalf("write fixture: %v", err)
	}
	return path, emit.Target{Dir: dir, Filename: name, Package: "x"}
}

// writePrevManifest writes a previous-run manifest at the
// brand-derived manifest path so the prune command finds it.
func writePrevManifest(t *testing.T, env *cli.Env, targets ...emit.Target) {
	t.Helper()
	stateDir := env.StateDir()
	if err := os.MkdirAll(stateDir, 0o750); err != nil {
		t.Fatalf("mkdir state: %v", err)
	}
	m := manifest.New("prev-run-id")
	m.Brand = env.Brand
	for _, tgt := range targets {
		m.Add(
			manifest.Output{
				Target:  tgt,
				Plugins: []manifest.PluginAttribution{{Name: "seedgen"}},
				Hash:    "sha256:dummy",
			},
		)
	}
	if err := manifest.Write(env.ManifestPath(), m); err != nil {
		t.Fatalf("write prev manifest: %v", err)
	}
}

// TestPruneCommand_DeletesStaleWithMarker covers the happy path:
// a file claimed by the previous manifest, no longer claimed by
// the current run, with the canonical generated marker — gets
// deleted.
func TestPruneCommand_DeletesStaleWithMarker(t *testing.T) {
	t.Parallel()

	t.Run("file with generated marker is deleted", func(t *testing.T) {
		t.Parallel()
		env, stdout, _ := freshEnv(t, "eidos")
		path, target := seedStaleFile(t, env.Workdir, env.Brand, "out", "stale.go")
		writePrevManifest(t, env, target)
		cmd := &cli.PruneCommand{Config: cli.PruneConfig{
			Plugins: []plugin.Plugin{
				stubFrontend{name: "fe"},
				stubBackend{name: "be", lang: "stub"},
			},
		}}
		if code := cmd.Execute(t.Context(), env); code != cli.ExitOK {
			t.Fatalf("Execute = %d, want ExitOK", code)
		}
		if _, err := os.Stat(path); !errors.Is(err, os.ErrNotExist) {
			t.Fatalf("stale file should be deleted; stat err=%v", err)
		}
		if !strings.Contains(stdout.String(), "deleted:") {
			t.Fatalf("expected deletion line in stdout; got %q", stdout.String())
		}
	})
}

// TestPruneCommand_SkipsWithoutMarker covers the safety guard:
// a file the manifest claims but whose first line lacks the
// generated marker (someone manually edited it) is NOT deleted.
func TestPruneCommand_SkipsWithoutMarker(t *testing.T) {
	t.Parallel()

	t.Run("file without generated marker survives prune", func(t *testing.T) {
		t.Parallel()
		env, stdout, _ := freshEnv(t, "eidos")
		dir := filepath.Join(env.Workdir, "out")
		if err := os.MkdirAll(dir, 0o750); err != nil {
			t.Fatalf("mkdir: %v", err)
		}
		path := filepath.Join(dir, "edited.go")
		// No generated marker — the file appears human-authored.
		body := []byte("package x\n\nfunc Hello() {}\n")
		if err := os.WriteFile(path, body, 0o600); err != nil {
			t.Fatalf("write fixture: %v", err)
		}
		writePrevManifest(t, env, emit.Target{Dir: "out", Filename: "edited.go", Package: "x"})
		cmd := &cli.PruneCommand{Config: cli.PruneConfig{
			Plugins: []plugin.Plugin{
				stubFrontend{name: "fe"},
				stubBackend{name: "be", lang: "stub"},
			},
		}}
		if code := cmd.Execute(t.Context(), env); code != cli.ExitOK {
			t.Fatalf("Execute = %d, want ExitOK", code)
		}
		if _, err := os.Stat(path); err != nil {
			t.Fatalf("file without marker should survive; got %v", err)
		}
		if !strings.Contains(stdout.String(), "skipped") {
			t.Fatalf("expected skip count in stdout; got %q", stdout.String())
		}
	})
}

// TestPruneCommand_DryRunDoesNotDelete covers the DryRun mode:
// the command reports planned deletions without performing them.
func TestPruneCommand_DryRunDoesNotDelete(t *testing.T) {
	t.Parallel()

	t.Run("DryRun reports would-delete without removing", func(t *testing.T) {
		t.Parallel()
		env, stdout, _ := freshEnv(t, "eidos")
		path, target := seedStaleFile(t, env.Workdir, env.Brand, "out", "candidate.go")
		writePrevManifest(t, env, target)
		cmd := &cli.PruneCommand{Config: cli.PruneConfig{
			DryRun: true,
			Plugins: []plugin.Plugin{
				stubFrontend{name: "fe"},
				stubBackend{name: "be", lang: "stub"},
			},
		}}
		if code := cmd.Execute(t.Context(), env); code != cli.ExitOK {
			t.Fatalf("Execute = %d, want ExitOK", code)
		}
		if _, err := os.Stat(path); err != nil {
			t.Fatalf("DryRun must not delete; got %v", err)
		}
		out := stdout.String()
		if !strings.Contains(out, "would delete:") {
			t.Fatalf("expected would-delete line; got %q", out)
		}
		if !strings.Contains(out, "dry-run") {
			t.Fatalf("expected dry-run summary; got %q", out)
		}
	})
}

